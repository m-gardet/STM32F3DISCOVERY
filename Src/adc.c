int32_t adc_read(uint32_t channel)
{
    int32_t value = -1;
    uint32_t timeout_in_millisecond = 2000;

    ADC_ChannelConfTypeDef sConfig;
    sConfig.Channel = channel;
    sConfig.Rank = 1;
    sConfig.SingleDiff = ADC_SINGLE_ENDED;
    sConfig.SamplingTime = ADC_SAMPLETIME_601CYCLES_5 ; //ADC_SAMPLETIME_1CYCLE_5;
    sConfig.OffsetNumber = ADC_OFFSET_NONE;
    sConfig.Offset = 0;
    if(HAL_ADC_ConfigChannel(&hadc1, &sConfig) != HAL_OK)
    {
        return value;
    }

    if(HAL_ADC_Start(&hadc1) != HAL_OK)
    {
        return value;
    }

    if (HAL_ADC_PollForConversion(&hadc1, timeout_in_millisecond) == HAL_OK)
    {
        value = HAL_ADC_GetValue(&hadc1);
    }

    return value;

}

